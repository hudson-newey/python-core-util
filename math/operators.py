def add(x: float, y: float) -> float:
    return x + y


def sub(x: float, y: float) -> float:
    return x - y


def mul(x: float, y: float) -> float:
    return x * y


def div(x: float, y: float) -> float:
    return x / y


def pow(x: float, y: float) -> float:
    return x**y


def sqrt(x: float) -> float:
    from math import sqrt

    return sqrt(x)
